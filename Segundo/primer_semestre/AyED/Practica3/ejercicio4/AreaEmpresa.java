package ejercicio4;

public class AreaEmpresa {
	private String ident;
	private int time;
	
	public AreaEmpresa(String ident, int time) {
		this.ident = ident;
		this.time = time;
	}
	public String getIdent() {
		return ident;
	}
	public void setIdent(String ident) {
		this.ident = ident;
	}
	public int getTime() {
		return time;
	}
	public void setTime(int time) {
		this.time = time;
	}
	
	
}
